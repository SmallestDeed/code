package com.nork.product.model.vo;

import java.io.Serializable;

/**
 * Created by xiaoxc on 2018/3/15 0015.
 */
public class ProductCeilingVO implements Serializable {

    private static final long serialVersionUID = 1L;

    // 天花截面数据
    private String ceilingCrossSectionData;

    // 天花区域标识
    private String regionMark;

    // 天花款式
    private String styleCode;

    // 射灯距离天花顶的高度
    private String distanceCeilingTopHeight;

    // 天花最小高度
    private String minHeight;

    //天花高度
    private String ceilingHeight;

    //天花宽度
    private String ceilingWidth;

    public String getCeilingCrossSectionData() {
        return ceilingCrossSectionData;
    }

    public void setCeilingCrossSectionData(String ceilingCrossSectionData) {
        this.ceilingCrossSectionData = ceilingCrossSectionData;
    }

    public String getStyleCode() {
        return styleCode;
    }

    public void setStyleCode(String styleCode) {
        this.styleCode = styleCode;
    }


    public String getRegionMark() {
        return regionMark;
    }

    public void setRegionMark(String regionMark) {
        this.regionMark = regionMark;
    }

    public String getDistanceCeilingTopHeight() {
        return distanceCeilingTopHeight;
    }

    public void setDistanceCeilingTopHeight(String distanceCeilingTopHeight) {
        this.distanceCeilingTopHeight = distanceCeilingTopHeight;
    }

    public String getMinHeight() {
        return minHeight;
    }

    public void setMinHeight(String minHeight) {
        this.minHeight = minHeight;
    }

    public String getCeilingHeight() {
        return ceilingHeight;
    }

    public void setCeilingHeight(String ceilingHeight) {
        this.ceilingHeight = ceilingHeight;
    }

    public String getCeilingWidth() {
        return ceilingWidth;
    }

    public void setCeilingWidth(String ceilingWidth) {
        this.ceilingWidth = ceilingWidth;
    }
}
