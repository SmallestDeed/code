package com.nork.common.async;

import com.nork.design.model.RenderConfig;
import com.nork.task.model.SysTask;

public class AssembleRenderTaskFileParameter {

	private SysTask sysTask ;
	private RenderConfig renderConfig;
	
	public AssembleRenderTaskFileParameter(SysTask sysTask,RenderConfig renderConfig){
		this.sysTask = sysTask;
		this.renderConfig = renderConfig;
	}
	
	public SysTask getSysTask() {
		return sysTask;
	}

	public void setSysTask(SysTask sysTask) {
		this.sysTask = sysTask;
	}
	
	public RenderConfig getRenderConfig() {
		return renderConfig;
	}
	
	public void setRenderConfig(RenderConfig renderConfig) {
		this.renderConfig = renderConfig;
	}
	
}
