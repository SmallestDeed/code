package com.sandu.common.util;

import java.util.Map;

/**
 * 资源分布式存储工具类
 * 
 * @author huangsongbo
 */
//@Configuration
public class ResDistributeUtils {

	/**
	 * 分布式未加密文件上传路径配置
	 * app.upload.root.distribute
	 */
	public static String noEncryptDistribute ;

	/**
	 * 分布式加密文件上传路径配置
	 * app.resources.encrypt.upload.root.distribute
	 */
	public static  String encryptDistribute;

	/**
	 * 域名配置
	 * app.resources.url.distribute
	 */
    public static  String urlDistribute ;

	public static Map<String, String> noEncryptDistributeMap ;
	
	public static Map<String, String> encryptDistributeMap;
	
	public static Map<String, String> urlDistributeMap ;

	public static String getNoEncryptDistribute() {
		return noEncryptDistribute;
	}

	public static void setNoEncryptDistribute(String noEncryptDistribute) {
		ResDistributeUtils.noEncryptDistribute = noEncryptDistribute;
	}

	public static String getEncryptDistribute() {
		return encryptDistribute;
	}

	public static void setEncryptDistribute(String encryptDistribute) {
		ResDistributeUtils.encryptDistribute = encryptDistribute;
	}

	public static String getUrlDistribute() {
		return urlDistribute;
	}

	public static void setUrlDistribute(String urlDistribute) {
		ResDistributeUtils.urlDistribute = urlDistribute;
	}

	public static Map<String, String> getNoEncryptDistributeMap() {
		return noEncryptDistributeMap;
	}

	public static void setNoEncryptDistributeMap(Map<String, String> noEncryptDistributeMap) {
		ResDistributeUtils.noEncryptDistributeMap = noEncryptDistributeMap;
	}

	public static Map<String, String> getEncryptDistributeMap() {
		return encryptDistributeMap;
	}

	public static void setEncryptDistributeMap(Map<String, String> encryptDistributeMap) {
		ResDistributeUtils.encryptDistributeMap = encryptDistributeMap;
	}

	public static Map<String, String> getUrlDistributeMap() {
		return urlDistributeMap;
	}

	public static void setUrlDistributeMap(Map<String, String> urlDistributeMap) {
		ResDistributeUtils.urlDistributeMap = urlDistributeMap;
	}
}
