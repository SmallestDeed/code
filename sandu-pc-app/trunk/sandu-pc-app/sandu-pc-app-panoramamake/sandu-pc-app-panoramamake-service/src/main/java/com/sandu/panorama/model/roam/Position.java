package com.sandu.panorama.model.roam;

import lombok.Data;

import java.io.Serializable;

/**
 * Created by Administrator on 2017/7/11.
 */
@Data
public class Position implements Serializable{

    private double x;

    private double y;

    private double z;

    @Override
    public String toString(){
        return "\"position\" : {" +
                "\"x\" :" + x +
                ",\"y\" :" + y +
                ",\"z\" :" + z +
                "}";
    }

}
