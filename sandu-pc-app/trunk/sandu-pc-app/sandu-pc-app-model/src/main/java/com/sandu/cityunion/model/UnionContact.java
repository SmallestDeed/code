package com.sandu.cityunion.model;

import com.sandu.common.model.BaseModel;
import lombok.Data;

import java.io.Serializable;

@Data
public class UnionContact extends BaseModel implements Serializable {

    /** 用户ID **/
    private Integer userId;
    /** 联系人姓名 **/
    private String name;
    /** 联系人电话 **/
    private String phone;
    /** 头像 **/
    private Integer picId;

    @Override
    public String toString(){
        return "UnionContact{" +
                "id : " + this.getId() +
                "userId : " + userId +
                ",name : " + name +
                ",phone : " + phone +
                ",picId : " + picId +
                "}";
    }

}
