package com.sandu.system.model;

import java.io.Serializable;
import java.util.List;

/**
 * 用于读取背景墙优先级配置(onekeydesign.product.beijingType.priority)
 * 
 * @author huangsongbo
 *
 */
public class BeijingTypePriorityBean implements Serializable{

	private static final long serialVersionUID = 6606768138211301057L;

	/**
	 * 某大类下背景墙分类
	 */
	private List<String> wallTypePriority;

	/**
	 * 备用匹配方案
	 */
	private List<String> standby;
	
	public List<String> getStandby() {
		return standby;
	}

	public void setStandby(List<String> standby) {
		this.standby = standby;
	}

	public List<String> getWallTypePriority() {
		return wallTypePriority;
	}

	public void setWallTypePriority(List<String> wallTypePriority) {
		this.wallTypePriority = wallTypePriority;
	}
	
}
