public enum UserType {

	/**
	 * 内部用户
	 */
	INTERNAL(1, "内部用户"),

	/**
	 * 厂商
	 */
	COMPANY(2, "厂商"),

	/**
	 * 经销商
	 */
	DEALERS(3, "经销商"),

	/**
	 * 设计师
	 */
	DESIGNER(4, "设计师"),

	/**
	 * 设计公司
	 */
	DESIGNER_COMPANY(5, "设计公司"),

	/**
	 * 装修公司
	 */
	DECORATE(6, "装修公司"),

	/**
	 * 学校(培训机构)
	 */
	SCHOOL(7, "学校(培训机构)"),

	/**
	 * 普通
	 */
	COMMON(8, "普通");

	private Integer value;
	private String reamk;

	public Integer getValue() {
		return value;
	}

	public String getReamk() {
		return reamk;
	}

	UserType(Integer value) {
		this.value = value;
	}

	UserType(Integer value, String reamk) {
		this.value = value;
		this.reamk = reamk;
	}
}
