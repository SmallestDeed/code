public enum HouseType {

	/**
	 * 客厅
	 */
	LIVING_ROOM(1, "客厅"),

	/**
	 * 餐厅
	 */
	DINING_ROOM(2, "餐厅"),

	/**
	 * 客餐厅
	 */
	RESTAURANT(3, "客餐厅"),

	/**
	 * 卧室
	 */
	ROOM(4, "卧室"),

	/**
	 * 厨房
	 */
	KITCHEN(5, "厨房"),

	/**
	 * 卫生间
	 */
	TOILET(6, "卫生间"),

	/**
	 * 书房
	 */
	STUDY(7, "书房"),

	/**
	 * 衣帽间
	 */
	CLOAKROOM(8, "衣帽间"),

	/**
	 * 阳台
	 */
	BALCONY(9, "阳台"),

	/**
	 * 入户花园
	 */
	GARDEN(10, "入户花园"),

	/**
	 * 天井
	 */
	COURTYARD(11, "天井"),

	/**
	 * 其它
	 */
	OTHER_HOUSE(12, "其它"),

	/**
	 * 未知
	 */
	UNKNOWN(-1, "未知");

	private int value;
	private String name;

	public int getValue() {
		return value;
	}

	public String getName() {
		return name;
	}

	HouseType(int value, String name) {
		this.value = value;
		this.name = name;
	}
}
