package com.sandu.exception;

import com.sandu.common.constant.ResponseEnum;

/**
 * Description:
 *
 * @author 何文
 * @version 1.0 Company:Sandu Copyright:Copyright(c)2017
 * @date 2017/12/28
 */
public class BusinessException extends AppException {

	/**
	 * 
	 */
	private static final long serialVersionUID = 1L;

	public BusinessException(boolean flag, ResponseEnum responseEnum) {
		super(flag, responseEnum);
	}

	public BusinessException(boolean flag, ResponseEnum responseEnum, Throwable throwable) {
		super(flag, responseEnum, throwable);
	}

	public ResponseEnum getResponseEnum() {
		return super.getResponseEnum();
	}

	public void setResponseEnum(ResponseEnum responseEnum) {
		super.setResponseEnum(responseEnum);
	}

	public boolean isFlag() {
		return super.isFlag();
	}

	public void setFlag(boolean flag) {
		super.setFlag(flag);
	}

	@Override
	public String toString() {
		return super.toString();
	}
}
