package com.nork.design.model;

import java.io.Serializable;

/**
 * @Author chenqiang
 * @Description
 * @Date 2018/9/12 0012 14:56
 * @Modified By
 */
public class Rotation implements Serializable{
    private static final long serialVersionUID = -5663500221984422793L;

    private Double x;
    private Double y;
    private Double z;

    public Double getX() {
        return x;
    }

    public void setX(Double x) {
        this.x = x;
    }

    public Double getY() {
        return y;
    }

    public void setY(Double y) {
        this.y = y;
    }

    public Double getZ() {
        return z;
    }

    public void setZ(Double z) {
        this.z = z;
    }
}
