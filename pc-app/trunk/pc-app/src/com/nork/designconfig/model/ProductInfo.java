package com.nork.designconfig.model;

import java.io.Serializable;

/**
 * @Author chenqiang
 * @Description
 * @Date 2018/12/11 0011 15:55
 * @Modified By
 */
public class ProductInfo implements Serializable {
    private static final long serialVersionUID = -3284427711418748338L;

    private Integer id;
    private String productName;
    private Integer putawayState;
    private Integer productTypeValue;
    private String valuekey;
    private Integer productSmallTypeValue;
    private String smallValueKey;

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public String getProductName() {
        return productName;
    }

    public void setProductName(String productName) {
        this.productName = productName;
    }

    public Integer getProductTypeValue() {
        return productTypeValue;
    }

    public void setProductTypeValue(Integer productTypeValue) {
        this.productTypeValue = productTypeValue;
    }

    public String getValuekey() {
        return valuekey;
    }

    public void setValuekey(String valuekey) {
        this.valuekey = valuekey;
    }

    public Integer getProductSmallTypeValue() {
        return productSmallTypeValue;
    }

    public void setProductSmallTypeValue(Integer productSmallTypeValue) {
        this.productSmallTypeValue = productSmallTypeValue;
    }

    public String getSmallValueKey() {
        return smallValueKey;
    }

    public void setSmallValueKey(String smallValueKey) {
        this.smallValueKey = smallValueKey;
    }

    public Integer getPutawayState() {
        return putawayState;
    }

    public void setPutawayState(Integer putawayState) {
        this.putawayState = putawayState;
    }
}
