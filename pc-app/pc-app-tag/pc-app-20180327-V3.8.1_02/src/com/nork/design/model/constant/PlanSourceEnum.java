public enum PlanSourceEnum {

    /**
     * 内部方案
     */
	diy("diy"),

    /**
     * 外部方案
     */
	deliver("deliver"),

    /**
     * 分享方案
     */
	share("share");

    private String code;

    PlanSourceEnum(String code) {
        this.code = code;
    }

    public String getCode() {
        return code;
    }
	
}
