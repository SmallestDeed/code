package com.nork.cityunion.model.vo;

import java.io.Serializable;
import java.util.List;

/**
 * Created by kono on 2018/1/21 0021.
 */
public class UnionGroupVO implements Serializable {
    private static final long serialVersionUID = 1L;

    //组id
    private Integer groupId;
    //组名称
    private String groupName;
    //组详情
    private List<UnionGroupDetailsVO> groupDetailsList;

    public Integer getGroupId() {
        return groupId;
    }

    public void setGroupId(Integer groupId) {
        this.groupId = groupId;
    }

    public String getGroupName() {
        return groupName;
    }

    public void setGroupName(String groupName) {
        this.groupName = groupName;
    }

    public List<UnionGroupDetailsVO> getGroupDetailsList() {
        return groupDetailsList;
    }

    public void setGroupDetailsList(List<UnionGroupDetailsVO> groupDetailsList) {
        this.groupDetailsList = groupDetailsList;
    }
}
