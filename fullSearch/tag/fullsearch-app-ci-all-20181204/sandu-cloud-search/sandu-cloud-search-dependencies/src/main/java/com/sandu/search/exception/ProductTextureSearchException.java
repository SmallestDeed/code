package com.sandu.search.exception;

/**
 * 产品材质搜索异常
 *
 * @date 20171215
 * @auth pengxuangang
 */
public class ProductTextureSearchException extends Exception {
    public ProductTextureSearchException() {
    }

    public ProductTextureSearchException(String message) {
        super(message);
    }

    public ProductTextureSearchException(String message, Throwable cause) {
        super(message, cause);
    }

    public ProductTextureSearchException(Throwable cause) {
        super(cause);
    }

    public ProductTextureSearchException(String message, Throwable cause, boolean enableSuppression, boolean writableStackTrace) {
        super(message, cause, enableSuppression, writableStackTrace);
    }
}
