package com.sandu.search.exception;

/**
 * 品牌搜索异常
 *
 * @date 20171215
 * @auth pengxuangang
 */
public class ProductNameSearchException extends Exception {
    public ProductNameSearchException() {
    }

    public ProductNameSearchException(String message) {
        super(message);
    }

    public ProductNameSearchException(String message, Throwable cause) {
        super(message, cause);
    }

    public ProductNameSearchException(Throwable cause) {
        super(cause);
    }

    public ProductNameSearchException(String message, Throwable cause, boolean enableSuppression, boolean writableStackTrace) {
        super(message, cause, enableSuppression, writableStackTrace);
    }
}
