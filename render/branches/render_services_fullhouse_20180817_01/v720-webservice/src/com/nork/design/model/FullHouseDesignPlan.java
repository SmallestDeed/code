package com.nork.design.model;

import java.io.Serializable;
import java.util.Date;

/**
 * 全屋方案表数据模型
 */

public class FullHouseDesignPlan implements Serializable {
    /**
     * 主键
     */
    private Integer id;
    /**
     * 通用唯一识别码
     */
    private String uuid;
    /**
     * 全屋方案编码
     */
    private String planCode;
    /**
     * 全屋方案名称
     */
    private String planName;
    /**
     * 全屋方案风格ID
     */
    private Integer planStyleId;
    /**
     * 全屋方案风格
     */
    private String planStyleName;
    /**
     * 方案缩略图ID
     */
    private Integer planPicId;
    /**
     * 方案描述
     */
    private String planDescribe;
    /**
     * 公司ID
     */
    private Integer companyId;
    /**
     * 品牌ID
     */
    private Integer brandIds;
    /**
     * 设计师ID
     */
    private Integer userId;
    /**
     * 方案来源类型(1:内部制作,2:装进我家)
     */
    private Integer sourceType;
    /**
     * 来源方案ID
     */
    private Integer sourcePlanId;
    /**
     * 方案交付次数
     */
    private Integer allotCount;
    /**
     * 方案公开状态(0:未公开,1:已公开)
     */
    private Integer openState;
    /**
     * 720 UUID
     */
    private String vrResourceUuid;
    /**
     * 方案版本
     */
    private Integer version;
    /**
     * 公开时间
     */
    private Date openTime;
    /**
     * 创建者
     */
    private String creator;
    /**
     * 创建时间
     */
    private Date gmtCreate;
    /**
     * 修改者
     */
    private String modifier;
    /**
     * 修改时间
     */
    private Date gmtModified;
    /**
     * 是否删除
     */
    private Integer isDeleted;
    /**
     * 备注
     */
    private String remark;

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public String getUuid() {
        return uuid;
    }

    public void setUuid(String uuid) {
        this.uuid = uuid;
    }

    public String getPlanCode() {
        return planCode;
    }

    public void setPlanCode(String planCode) {
        this.planCode = planCode;
    }

    public String getPlanName() {
        return planName;
    }

    public void setPlanName(String planName) {
        this.planName = planName;
    }

    public Integer getPlanStyleId() {
        return planStyleId;
    }

    public void setPlanStyleId(Integer planStyleId) {
        this.planStyleId = planStyleId;
    }

    public String getPlanStyleName() {
        return planStyleName;
    }

    public void setPlanStyleName(String planStyleName) {
        this.planStyleName = planStyleName;
    }

    public Integer getPlanPicId() {
        return planPicId;
    }

    public void setPlanPicId(Integer planPicId) {
        this.planPicId = planPicId;
    }

    public String getPlanDescribe() {
        return planDescribe;
    }

    public void setPlanDescribe(String planDescribe) {
        this.planDescribe = planDescribe;
    }

    public Integer getCompanyId() {
        return companyId;
    }

    public void setCompanyId(Integer companyId) {
        this.companyId = companyId;
    }

    public Integer getBrandIds() {
        return brandIds;
    }

    public void setBrandIds(Integer brandIds) {
        this.brandIds = brandIds;
    }

    public Integer getUserId() {
        return userId;
    }

    public void setUserId(Integer userId) {
        this.userId = userId;
    }

    public Integer getSourceType() {
        return sourceType;
    }

    public void setSourceType(Integer sourceType) {
        this.sourceType = sourceType;
    }

    public Integer getSourcePlanId() {
        return sourcePlanId;
    }

    public void setSourcePlanId(Integer sourcePlanId) {
        this.sourcePlanId = sourcePlanId;
    }

    public Integer getAllotCount() {
        return allotCount;
    }

    public void setAllotCount(Integer allotCount) {
        this.allotCount = allotCount;
    }

    public Integer getOpenState() {
        return openState;
    }

    public void setOpenState(Integer openState) {
        this.openState = openState;
    }

    public String getVrResourceUuid() {
        return vrResourceUuid;
    }

    public void setVrResourceUuid(String vrResourceUuid) {
        this.vrResourceUuid = vrResourceUuid;
    }

    public Integer getVersion() {
        return version;
    }

    public void setVersion(Integer version) {
        this.version = version;
    }

    public Date getOpenTime() {
        return openTime;
    }

    public void setOpenTime(Date openTime) {
        this.openTime = openTime;
    }

    public String getCreator() {
        return creator;
    }

    public void setCreator(String creator) {
        this.creator = creator;
    }

    public Date getGmtCreate() {
        return gmtCreate;
    }

    public void setGmtCreate(Date gmtCreate) {
        this.gmtCreate = gmtCreate;
    }

    public String getModifier() {
        return modifier;
    }

    public void setModifier(String modifier) {
        this.modifier = modifier;
    }

    public Date getGmtModified() {
        return gmtModified;
    }

    public void setGmtModified(Date gmtModified) {
        this.gmtModified = gmtModified;
    }

    public Integer getIsDeleted() {
        return isDeleted;
    }

    public void setIsDeleted(Integer isDeleted) {
        this.isDeleted = isDeleted;
    }

    public String getRemark() {
        return remark;
    }

    public void setRemark(String remark) {
        this.remark = remark;
    }
}