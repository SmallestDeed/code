package com.nork.cityunion.model.vo;


import java.io.Serializable;
import java.util.List;

/**
 * 联盟门店分组信息
 * @author chenm
 * @date 2018/10/30
 */

public class UnionGroupVo implements Serializable {

    private static final long serialVersionUID = 1L;

    private Integer id;
    /** 联盟门店名称 **/
    private String groupName ;
    /**
     * 关联企业信息
     * (720制作中显示的是企业信息）
     */
    private List<UnionGroupDetailsVo> detailsVos;

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public String getGroupName() {
        return groupName;
    }

    public void setGroupName(String groupName) {
        this.groupName = groupName;
    }

    public List<UnionGroupDetailsVo> getDetailsVos() {
        return detailsVos;
    }

    public void setDetailsVos(List<UnionGroupDetailsVo> detailsVos) {
        this.detailsVos = detailsVos;
    }

}
