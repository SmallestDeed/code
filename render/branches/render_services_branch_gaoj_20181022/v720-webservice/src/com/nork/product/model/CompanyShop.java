package com.nork.product.model;

import java.io.Serializable;

/****
 * 店铺信息
 * @author Administrator
 */
public class CompanyShop implements Serializable {
	// 店铺主键
	private Integer shopId;
	// "公司ID"
	private long companyId;
	// "店铺名称"
	private String shopName;
	// "店铺Logo图片资源ID"
	private long logoId;
	// "店铺商品分类ID集合"
	private String productCategoryIds;
	// "店铺所在区域Code"
	private int longAreaCode;
	// "店铺所在县城或城区ID"
	private int areaId;
	// "店铺详细地址"
	private String shopAddress;
	// "访问次数"
	private int visitCount;
	// "好评率"
	private float praiseRate;
	// "销量"
	private int salesVolume;
	// "收藏数量"
	private int collectionCount;
	// "点赞数量"
	private int likeCount;
	// "评论数量"
	private int commentCount;
	// 店铺logo地址
	private String shopLogoPath;

	public Integer getShopId() {
		return shopId;
	}

	public void setShopId(Integer shopId) {
		this.shopId = shopId;
	}

	public String getShopLogoPath() {
		return shopLogoPath;
	}

	public void setShopLogoPath(String shopLogoPath) {
		this.shopLogoPath = shopLogoPath;
	}

	public long getCompanyId() {
		return companyId;
	}

	public void setCompanyId(long companyId) {
		this.companyId = companyId;
	}

	public String getShopName() {
		return shopName;
	}

	public void setShopName(String shopName) {
		this.shopName = shopName;
	}

	public String getProductCategoryIds() {
		return productCategoryIds;
	}

	public void setProductCategoryIds(String productCategoryIds) {
		this.productCategoryIds = productCategoryIds;
	}

	public int getAreaId() {
		return areaId;
	}

	public void setAreaId(int areaId) {
		this.areaId = areaId;
	}

	public String getShopAddress() {
		return shopAddress;
	}

	public void setShopAddress(String shopAddress) {
		this.shopAddress = shopAddress;
	}

	public int getVisitCount() {
		return visitCount;
	}

	public void setVisitCount(int visitCount) {
		this.visitCount = visitCount;
	}

	public float getPraiseRate() {
		return praiseRate;
	}

	public void setPraiseRate(float praiseRate) {
		this.praiseRate = praiseRate;
	}

	public long getLogoId() {
		return logoId;
	}

	public void setLogoId(long logoId) {
		this.logoId = logoId;
	}

	public int getLongAreaCode() {
		return longAreaCode;
	}

	public void setLongAreaCode(int longAreaCode) {
		this.longAreaCode = longAreaCode;
	}

	public int getSalesVolume() {
		return salesVolume;
	}

	public void setSalesVolume(int salesVolume) {
		this.salesVolume = salesVolume;
	}

	public int getCollectionCount() {
		return collectionCount;
	}

	public void setCollectionCount(int collectionCount) {
		this.collectionCount = collectionCount;
	}

	public int getLikeCount() {
		return likeCount;
	}

	public void setLikeCount(int likeCount) {
		this.likeCount = likeCount;
	}

	public int getCommentCount() {
		return commentCount;
	}

	public void setCommentCount(int commentCount) {
		this.commentCount = commentCount;
	}

}
