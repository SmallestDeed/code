package com.nork.mobile.model.search;

import java.io.Serializable;
/**
 * 移动端设计方案收藏 search model
 * @author yangzhun
 *
 */
public class DesignPlanCollectModel implements Serializable {

	 /**
     * 收藏夹业务UUID
     */
    private String businessId;
    /**
     * 收藏用户ID
     */
    private Integer userId;
    /**
     * 设计方案id
     */
    private Integer designPlanId;
    /**
     * 收藏夹里面显示的标题
     */
    private String title;
    /**
     * 房间类型
     */
    private String spaceType;
    /**
     * 设计方案风格
     */
    private String designPlanStyle;
    
    private String token;
    /**
     * 是否已经收藏，true：已收藏；false:未收藏
     */
    private String isCollect;
    
    
	public String getIsCollect() {
		return isCollect;
	}
	public void setIsCollect(String isCollect) {
		this.isCollect = isCollect;
	}
    
	public String getToken() {
		return token;
	}
	public void setToken(String token) {
		this.token = token;
	}
	public String getBusinessId() {
		return businessId;
	}
	public void setBusinessId(String businessId) {
		this.businessId = businessId;
	}
	public Integer getUserId() {
		return userId;
	}
	public void setUserId(Integer userId) {
		this.userId = userId;
	}
	public Integer getDesignPlanId() {
		return designPlanId;
	}
	public void setDesignPlanId(Integer designPlanId) {
		this.designPlanId = designPlanId;
	}
	public String getTitle() {
		return title;
	}
	public void setTitle(String title) {
		this.title = title;
	}
	public String getSpaceType() {
		return spaceType;
	}
	public void setSpaceType(String spaceType) {
		this.spaceType = spaceType;
	}
	public String getDesignPlanStyle() {
		return designPlanStyle;
	}
	public void setDesignPlanStyle(String designPlanStyle) {
		this.designPlanStyle = designPlanStyle;
	}
    
    
}
