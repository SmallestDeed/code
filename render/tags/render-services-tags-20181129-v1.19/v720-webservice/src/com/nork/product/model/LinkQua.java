public class LinkQua {

    private double x;
    private double y;
    private double z;
    private Normalized eulerAngles;
    private double magnitude;
    private double sqrMagnitude;

    public double getX() {
        return x;
    }

    public void setX(double x) {
        this.x = x;
    }

    public double getY() {
        return y;
    }

    public void setY(double y) {
        this.y = y;
    }

    public double getZ() {
        return z;
    }

    public void setZ(double z) {
        this.z = z;
    }

    public Normalized getEulerAngles() {
        return eulerAngles;
    }

    public void setEulerAngles(Normalized eulerAngles) {
        this.eulerAngles = eulerAngles;
    }

    public double getMagnitude() {
        return magnitude;
    }

    public void setMagnitude(double magnitude) {
        this.magnitude = magnitude;
    }

    public double getSqrMagnitude() {
        return sqrMagnitude;
    }

    public void setSqrMagnitude(double sqrMagnitude) {
        this.sqrMagnitude = sqrMagnitude;
    }

}
