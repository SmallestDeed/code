package com.nork.common.async;

import javax.servlet.http.HttpServletRequest;

/**
 * Created by Administrator on 2017/2/20 0020.
 */
public class UpdateDesignPlanCacheParameter {

    private Integer designPlanId;
    private String houseId;
    private String livingId;
    private String residentialUnitsName;
    private Integer newFlag;
    private HttpServletRequest request;

    public UpdateDesignPlanCacheParameter(Integer designPlanId,String houseId,String livingId,String residentialUnitsName,Integer newFlag,HttpServletRequest request){
        this.designPlanId = designPlanId;
        this.houseId = houseId;
        this.livingId = livingId;
        this.residentialUnitsName = residentialUnitsName;
        this.newFlag = newFlag;
        this.request = request;
    }

    public Integer getDesignPlanId() {
        return designPlanId;
    }

    public void setDesignPlanId(Integer designPlanId) {
        this.designPlanId = designPlanId;
    }

    public String getHouseId() {
        return houseId;
    }

    public void setHouseId(String houseId) {
        this.houseId = houseId;
    }

    public String getLivingId() {
        return livingId;
    }

    public void setLivingId(String livingId) {
        this.livingId = livingId;
    }

    public String getResidentialUnitsName() {
        return residentialUnitsName;
    }

    public void setResidentialUnitsName(String residentialUnitsName) {
        this.residentialUnitsName = residentialUnitsName;
    }

    public Integer getNewFlag() {
        return newFlag;
    }

    public void setNewFlag(Integer newFlag) {
        this.newFlag = newFlag;
    }

    public HttpServletRequest getRequest() {
        return request;
    }

    public void setRequest(HttpServletRequest request) {
        this.request = request;
    }
}
