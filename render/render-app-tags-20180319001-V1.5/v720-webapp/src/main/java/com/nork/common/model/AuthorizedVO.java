package com.nork.common.model;

public class AuthorizedVO {
	private String msgId;
	private String terminalImei;
	private String authorizedCode;
	private String password;
	private Integer status;
	private String type;
	private String authorizedConfigId;
	private String  id;
	
	
	public String getType() {
		return type;
	}
	public void setType(String type) {
		this.type = type;
	}
	public String getAuthorizedConfigId() {
		return authorizedConfigId;
	}
	public void setAuthorizedConfigId(String authorizedConfigId) {
		this.authorizedConfigId = authorizedConfigId;
	}
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getMsgId() {
		return msgId;
	}
	public void setMsgId(String msgId) {
		this.msgId = msgId;
	}
	public String getTerminalImei() {
		return terminalImei;
	}
	public void setTerminalImei(String terminalImei) {
		this.terminalImei = terminalImei;
	}
	public String getAuthorizedCode() {
		return authorizedCode;
	}
	public void setAuthorizedCode(String authorizedCode) {
		this.authorizedCode = authorizedCode;
	}
	public String getPassword() {
		return password;
	}
	public void setPassword(String password) {
		this.password = password;
	}
	public Integer getStatus() {
		return status;
	}
	public void setStatus(Integer status) {
		this.status = status;
	}
	
	
	 
}
