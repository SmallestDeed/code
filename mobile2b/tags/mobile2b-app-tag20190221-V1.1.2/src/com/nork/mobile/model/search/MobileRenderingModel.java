package com.nork.mobile.model.search;

import java.io.Serializable;

import com.nork.common.model.Mapper;
import lombok.Data;

/**
 * 移动端 效果图 model
 * @author yangzhun
 *
 */
@Data
public class MobileRenderingModel extends Mapper implements Serializable {
    /**
     * 缩略图id
     */
    private Integer thumbId;
    /**
     * 缩略图封面
     */
    private String pic;
    /**
     * 缩略图对应方案的名称
     */
    private String name;
    /**
     * 创建时间
     */
    private String ctime;
    /**
     * 空间类型
     */
    private String type;
    /**
     * 面积
     */
    private String area;
    /**
     * 对应的设计方案id
     */
    private Integer planId; 
    /**
     * 创建人id
     */
    private Integer userId;
    /**
     * 一键发布状态
     */
    private Integer oneKeySt;
    /**
     * 公开状态
     */
    private Integer pubSt;
    /**
     * 设计方案副本id
     */
    private Integer cpId;
    
    private String token;
    /**方案空间类型 1：单空间方案，2：全屋方案,0:渲染任务**/
    private Integer planHouseType;

	/**
     * 风格
     */
    private Integer designStyleId;
	/**
	 * 缩略图id
	 */
	private String smallPicId;
	/**
	 * 查看渲染效果来源
	 * //推荐1效果图2 设计3
	 */
	private String detailsSeeType;
	/**
	 * 方案來源类型  space_function_id
	 * 就是   对应   空间类型的value（客餐厅等）
	 */
	public Integer spaceFunctionId;


}
