package com.nork.design.model;

import java.io.Serializable;
import java.util.Date;

/**
 * 全屋方案详情表数据模型
 */
public class FullHouseDesignPlanDetail implements Serializable {
    /**
     * 主键
     */
    private Integer id;
    /**
     * 通用唯一识别码
     */
    private String uuid;
    /**
     * 全屋方案ID
     */
    private Integer fullHousePlanId;
    /**
     * 单空间推荐方案组合的主方案ID
     */
    private Integer recommendedPlanGroupPrimaryId;
    /**
     * 推荐方案对应的效果图方案ID
     */
    private Integer renderScenePlanId;
    /**
     * 空间类型(1:客厅,2:餐厅,3:客餐厅,4:卧室,5:厨房,6:卫生间,7:书房,8:衣帽间)
     */
    private Integer spaceType;
    /**
     * 优先级
     */
    private Integer priorityLevel;
    /**
     * 方案组合中主方案的720ID
     */
    private Integer planRenderResourceId;
    /**
     * 推荐方案的720ID
     */
    private Integer planRecommendedResourceId;
    /**
     * 创建者
     */
    private String creator;
    /**
     * 创建时间
     */
    private Date gmtCreate;
    /**
     * 修改者
     */
    private String modifier;
    /**
     * 修改时间
     */
    private Date gmtModified;
    /**
     * 是否删除
     */
    private Integer isDeleted;
    /**
     * 备注
     */
    private String remark;



    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public String getUuid() {
        return uuid;
    }

    public void setUuid(String uuid) {
        this.uuid = uuid;
    }

    public Integer getFullHousePlanId() {
        return fullHousePlanId;
    }

    public void setFullHousePlanId(Integer fullHousePlanId) {
        this.fullHousePlanId = fullHousePlanId;
    }

    public Integer getRecommendedPlanGroupPrimaryId() {
        return recommendedPlanGroupPrimaryId;
    }

    public void setRecommendedPlanGroupPrimaryId(Integer recommendedPlanGroupPrimaryId) {
        this.recommendedPlanGroupPrimaryId = recommendedPlanGroupPrimaryId;
    }

    public Integer getRenderScenePlanId() {
        return renderScenePlanId;
    }

    public void setRenderScenePlanId(Integer renderScenePlanId) {
        this.renderScenePlanId = renderScenePlanId;
    }

    public Integer getSpaceType() {
        return spaceType;
    }

    public void setSpaceType(Integer spaceType) {
        this.spaceType = spaceType;
    }

    public Integer getPriorityLevel() {
        return priorityLevel;
    }

    public void setPriorityLevel(Integer priorityLevel) {
        this.priorityLevel = priorityLevel;
    }

    public Integer getPlanRenderResourceId() {
        return planRenderResourceId;
    }

    public void setPlanRenderResourceId(Integer planRenderResourceId) {
        this.planRenderResourceId = planRenderResourceId;
    }

    public Integer getPlanRecommendedResourceId() {
        return planRecommendedResourceId;
    }

    public void setPlanRecommendedResourceId(Integer planRecommendedResourceId) {
        this.planRecommendedResourceId = planRecommendedResourceId;
    }

    public String getCreator() {
        return creator;
    }

    public void setCreator(String creator) {
        this.creator = creator;
    }

    public Date getGmtCreate() {
        return gmtCreate;
    }

    public void setGmtCreate(Date gmtCreate) {
        this.gmtCreate = gmtCreate;
    }

    public String getModifier() {
        return modifier;
    }

    public void setModifier(String modifier) {
        this.modifier = modifier;
    }

    public Date getGmtModified() {
        return gmtModified;
    }

    public void setGmtModified(Date gmtModified) {
        this.gmtModified = gmtModified;
    }

    public Integer getIsDeleted() {
        return isDeleted;
    }

    public void setIsDeleted(Integer isDeleted) {
        this.isDeleted = isDeleted;
    }

    public String getRemark() {
        return remark;
    }

    public void setRemark(String remark) {
        this.remark = remark;
    }
}