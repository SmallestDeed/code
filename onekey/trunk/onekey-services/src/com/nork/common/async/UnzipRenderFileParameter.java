package com.nork.common.async;

import com.nork.onekeydesign.model.RenderConfig;

/***
 * 准备3d渲染,将3d模型文件解压到渲染目录参数
 * @author xiao.xc
 * @date 2016.06.21
 *
 */
public class UnzipRenderFileParameter {
	
	private Integer planId;
	private String rootDirName;
	private RenderConfig renderConfig;
	private String type;
	
	public UnzipRenderFileParameter(Integer planId,String rootDirName,RenderConfig renderConfig,String type){
		this.planId = planId;
		this.rootDirName = rootDirName;
		this.renderConfig = renderConfig;
		this.type = type;
	}

	public Integer getPlanId() {
		return planId;
	}

	public void setPlanId(Integer planId) {
		this.planId = planId;
	}

	public String getRootDirName() {
		return rootDirName;
	}

	public void setRootDirName(String rootDirName) {
		this.rootDirName = rootDirName;
	}

	public RenderConfig getRenderConfig() {
		return renderConfig;
	}

	public void setRenderConfig(RenderConfig renderConfig) {
		this.renderConfig = renderConfig;
	}

	public String getType() {
		return type;
	}

	public void setType(String type) {
		this.type = type;
	}
	
}
