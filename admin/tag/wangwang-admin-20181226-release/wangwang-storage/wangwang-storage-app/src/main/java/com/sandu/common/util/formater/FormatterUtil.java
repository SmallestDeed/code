public class FormatterUtil {

    private static final String DATE_FORMATTER = "yyyy-MM-dd";
    private static final String DATE_TIME_FORMATTER = "yyyy-MM-dd HH:mm:ss";
    private static final String PATH_FORMATTER = "yyyy/MM/dd/HH";
    private static final String NAME_FORMATTER = "yyyyMMddHHmmssSSS";


    public static final DateTimeFormatter dateFormatter = DateTimeFormatter.ofPattern(DATE_FORMATTER);
    public static final DateTimeFormatter dateTimeFormatter = DateTimeFormatter.ofPattern(DATE_TIME_FORMATTER);
    public static final DateTimeFormatter pathFormatter = DateTimeFormatter.ofPattern(PATH_FORMATTER);
    public static final DateTimeFormatter nameFormatter = DateTimeFormatter.ofPattern(NAME_FORMATTER);

}
