package com.sandu.api.solution.constant;

/**
 * CopyType class
 *
 * @author bvvy
 * @date 2018/04/02
 */
public enum CopyType {

    /**
     * 分享类型
     */
    SHARE((byte) 1), DELIVER((byte) 2),;

    CopyType(Byte code) {
        this.code = code;
    }

    private Byte code;

    public Byte getCode() {
        return code;
    }
}
