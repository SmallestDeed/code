package com.sandu.api.product.model.bo;

import lombok.Data;

import java.io.Serializable;
import java.util.List;

/**
 * @author Sandu
 */
@Data
public class ProductTextureInfo implements Serializable {
    /**
     * 贴图区域名称
     */
    private String name;
    /**
     * "贴图区域内可替换贴图信息")
     */
    private List<TextureInfo> affectTextures;
    /**
     * "贴图区域默认贴图")
     */
    private Integer defaultId;
    /**
     * "贴图区域内所有贴图ID")
     */
    private String textureIds;
    /**
     * 贴图编码
     */
    private String key;

    private String textureRegionName;
    /**
     * 区域材质默认高度
     */
    private Integer height;
    /**
     * 区域材质默认宽度
     */
    private Integer width;
}
