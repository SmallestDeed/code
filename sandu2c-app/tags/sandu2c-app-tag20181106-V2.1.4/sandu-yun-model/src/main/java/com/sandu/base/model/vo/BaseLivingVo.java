package com.sandu.base.model.vo;

import com.sandu.common.model.PageModel;

import java.io.Serializable;

/**
 * 小区VO
 *
 * @author
 */
public class BaseLivingVo implements Serializable {

    private Integer id;
    /**
     * 小区编码
     **/
    private String livingCode;
    /**
     * 小区名称
     **/
    private String livingName;
    /**
     * 省份编码
     **/
    private String areaId;
    /**
     * 市编码
     **/
    private String cityId;
    /**
     * 区域
     **/
    private String areaStr;
    /**
     * 省市长编码，用于右模糊查询
     **/
    private String sch_AreaLongCode_;
    /**
     * 用于模糊查询小区名称
     **/
    private String sch_LivingName_;
    
    private String houseCount;
    
    

 

	public String getHouseCount() {
		return houseCount;
	}

	public void setHouseCount(String houseCount) {
		this.houseCount = houseCount;
	}

	public static int getBaseLivingPagePagesize() {
        return PageModel.DEFAULT_PAGE_PAGESIZE;
    }

    public static int getBaseLivingPageCurrentpage() {
        return PageModel.DEFAULT_PAGE_CURRENTPAGE;
    }

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public String getLivingName() {
        return livingName;
    }

    public void setLivingName(String livingName) {
        this.livingName = livingName;
    }

    public String getAreaStr() {
        return areaStr;
    }

    public void setAreaStr(String areaStr) {
        this.areaStr = areaStr;
    }

    public String getAreaId() {
        return areaId;
    }

    public void setAreaId(String areaId) {
        this.areaId = areaId;
    }

    public String getCityId() {
        return cityId;
    }

    public void setCityId(String cityId) {
        this.cityId = cityId;
    }

    public String getLivingCode() {
        return livingCode;
    }

    public void setLivingCode(String livingCode) {
        this.livingCode = livingCode;
    }

    public String getSch_AreaLongCode_() {
        return sch_AreaLongCode_;
    }

    public void setSch_AreaLongCode_(String sch_AreaLongCode_) {
        this.sch_AreaLongCode_ = sch_AreaLongCode_;
    }

    public String getSch_LivingName_() {
        return sch_LivingName_;
    }

    public void setSch_LivingName_(String sch_LivingName_) {
        this.sch_LivingName_ = sch_LivingName_;
    }


}
