package com.sandu.designshare.model.vo;

import java.io.Serializable;

/**
 * Created by yanghz on 2017-08-02.
 */
public class GroupPicVo implements Serializable{
    //业务id(副本)
    private int bid;

    //缩略图path
    private String smallPicPath;

    //截图path
    private String screenPicPath;

    //对应的副本名称
    private String sceneName;

    //方案描述
    private  String description;

    //渲染类型
    private Integer renderingType;

    //720原图id（用于二维码查看）
    private int picId;
    //720原图的缩略图id 
    private int smallPicId;
    
    public int getSmallPicId() {
		return smallPicId;
	}

	public void setSmallPicId(int smallPicId) {
		this.smallPicId = smallPicId;
	}

	public int getPicId() {
        return picId;
    }

    public void setPicId(int picId) {
        this.picId = picId;
    }

    public Integer getRenderingType() {
        return renderingType;
    }

    public void setRenderingType(Integer renderingType) {
        this.renderingType = renderingType;
    }

    public int getBid() {
        return bid;
    }

    public void setBid(int bid) {
        this.bid = bid;
    }

    public String getDescription() {
        return description;
    }

    public void setDescription(String description) {
        this.description = description;
    }

    public String getSceneName() {
        return sceneName;
    }

    public void setSceneName(String sceneName) {
        this.sceneName = sceneName;
    }

    public String getSmallPicPath() {
        return smallPicPath;
    }

    public void setSmallPicPath(String smallPicPath) {
        this.smallPicPath = smallPicPath;
    }

    public String getScreenPicPath() {
        return screenPicPath;
    }

    public void setScreenPicPath(String screenPicPath) {
        this.screenPicPath = screenPicPath;
    }
}
