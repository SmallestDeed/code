package com.sandu.product.po;

import java.io.Serializable;

/**
 * 设计方案产品风格业务对象
 */
public class DesignProductStyleDicPo implements Serializable{

    private static final long serialVersionUID = 9084408999874903351L;

    //原始HouseType
    private Integer orgHouseType;
    //空间类型ID
    private Integer houseType;
    //设计方案风格ID
    private Integer baseProductStyleId;
    //风格名
    private String styleName;

    public Integer getOrgHouseType() {
        return orgHouseType;
    }

    public void setOrgHouseType(Integer orgHouseType) {
        this.orgHouseType = orgHouseType;
    }

    public Integer getHouseType() {
        return houseType;
    }

    public void setHouseType(Integer houseType) {
        this.houseType = houseType;
    }

    public Integer getBaseProductStyleId() {
        return baseProductStyleId;
    }

    public void setBaseProductStyleId(Integer baseProductStyleId) {
        this.baseProductStyleId = baseProductStyleId;
    }

    public String getStyleName() {
        return styleName;
    }

    public void setStyleName(String styleName) {
        this.styleName = styleName;
    }

    @Override
    public String toString() {
        return "DesignProductStyleDicPo{" +
                "orgHouseType=" + orgHouseType +
                ", houseType=" + houseType +
                ", baseProductStyleId=" + baseProductStyleId +
                ", styleName='" + styleName + '\'' +
                '}';
    }
}
