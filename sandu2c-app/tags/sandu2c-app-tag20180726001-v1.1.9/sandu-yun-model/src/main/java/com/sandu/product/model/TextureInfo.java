package com.sandu.product.model;

import java.io.Serializable;

public class TextureInfo implements Serializable {

    private static final long serialVersionUID = 7991974873311448997L;
    private Integer id;
    private Integer affectPrice;

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public Integer getAffectPrice() {
        return affectPrice;
    }

    public void setAffectPrice(Integer affectPrice) {
        this.affectPrice = affectPrice;
    }
}