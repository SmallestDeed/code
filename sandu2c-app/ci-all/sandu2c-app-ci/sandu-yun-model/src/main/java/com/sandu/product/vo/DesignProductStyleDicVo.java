package com.sandu.product.vo;

import com.sandu.designplan.vo.DesignPlanAreaVo;
import com.sandu.designplan.vo.DesignPlanStyleVo;

import java.io.Serializable;
import java.util.List;

/**
 * 设计方案产品风格视图对象
 */
public class DesignProductStyleDicVo implements Serializable{

    private static final long serialVersionUID = -2690049137137839244L;
    //空间类型
    private Integer houseType;
    //空间名
    private String houseName;
    //设计风格VO-List
    private List<DesignPlanStyleVo> designPlanStyleVoList;
    //设计方案面积LIST
    private List<DesignPlanAreaVo> designPlanAreaList;

    public Integer getHouseType() {
        return houseType;
    }

    public void setHouseType(Integer houseType) {
        this.houseType = houseType;
    }

    public String getHouseName() {
        return houseName;
    }

    public void setHouseName(String houseName) {
        this.houseName = houseName;
    }

    public List<DesignPlanStyleVo> getDesignPlanStyleVoList() {
        return designPlanStyleVoList;
    }

    public void setDesignPlanStyleVoList(List<DesignPlanStyleVo> designPlanStyleVoList) {
        this.designPlanStyleVoList = designPlanStyleVoList;
    }

    public List<DesignPlanAreaVo> getDesignPlanAreaList() {
        return designPlanAreaList;
    }

    public void setDesignPlanAreaList(List<DesignPlanAreaVo> designPlanAreaList) {
        this.designPlanAreaList = designPlanAreaList;
    }

    @Override
    public String toString() {
        return "DesignProductStyleDicVo{" +
                "houseType=" + houseType +
                ", houseName='" + houseName + '\'' +
                ", designPlanStyleVoList=" + designPlanStyleVoList +
                ", designPlanAreaList=" + designPlanAreaList +
                '}';
    }
}
