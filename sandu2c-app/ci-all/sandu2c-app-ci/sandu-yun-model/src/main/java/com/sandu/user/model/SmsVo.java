package com.sandu.user.model;

public class SmsVo {

	//验证码类型--注册
	public static final String SMS_CHECK_CODE_TYPE_REGISTER = "register";
	//验证码类型--修改密码
	public static final String SMS_CHECK_CODE_TYPE_MODIFY_PASSWORD = "updateMobile";

	private String code;//验证码
	private Long sendTime;//生成时间
	private Integer verifyCount;//验证次数
	private String type;//验证类型
	
	public String getType() {
		return type;
	}
	public void setType(String type) {
		this.type = type;
	}
	public Integer getVerifyCount() {
		return verifyCount;
	}
	public void setVerifyCount(Integer verifyCount) {
		this.verifyCount = verifyCount;
	}
	public String getCode() {
		return code;
	}
	public void setCode(String code) {
		this.code = code;
	}
	public Long getSendTime() {
		return sendTime;
	}
	public void setSendTime(Long sendTime) {
		this.sendTime = sendTime;
	}
	
	

}
